import { h, ref } from "../../lib/guide-mini-vue.esm.js";
const nextChildren = "oldChildren";
const prevChildren = "newChildren";

export default {
  name: "ArrayToArray",
  setup() {
    const isChange = ref(false);
    window.isChange = isChange;

    return {
      isChange,
    };
  },
  render() {
    const self = this;
    return self.isChange === true
      ? h("div", {}, nextChildren)
      : h("div", {}, prevChildren);
  },
};
